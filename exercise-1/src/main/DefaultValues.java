package main;

public class DefaultValues {
	
	
	static byte byteData;
	static short shortData;
	static int intData;
	static float floatData;
	static double doubleData;
	static long longData;
	static char charData;
	static boolean booleanData;
	
	public static void main(String[] args) {
		
		System.out.println("Byte: " + byteData + " Short: " + shortData);
		System.out.println("Int: " + intData + " Float: " + floatData);
		System.out.println("Double: " + doubleData + " Long: " + longData);
		System.out.println("Char: " + charData + " Boolean: " + booleanData);
	}
}
